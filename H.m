function y = H(P,T,phase)
z = Z(P,T,phase);
Tc = 154.6;
Pc = 5.046*10^6;
R = 8.314;
b = 0.0778*R*Tc*(1/Pc);

B = (b*P*10^5)/(R*T);

Tr = 298.15; 
c = 25.46*(T -Tr) + (0.7595*10^(-2))*(T^2 - (Tr)^2) - (0.7151*(1/3)*10^(-5))*(T^3 -(Tr)^3) + (0.32775*10^(-9))*(T^4-(Tr)^4);
d = T*diff_a(T);
sq = sqrt(2);
s = ((d-a(T))/(b*2*sq))*log((z + (1+sq)*B)./(z + (1-sq)*B));
v = R*T*(z-1);

y = v + s + c;